
public class DataHora
{
    protected String dia;
    protected String mes;
    protected String ano;
    protected String min;
    protected String sec;
    
    public DataHora(String dia, String mes, String ano, String min, String sec){
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
        this.min = min;
        this.sec = sec;
    }
    
}
